<a name="Destinations"><font size="6" color="maroon"> <b>Destinations</b></font> </a>
<br/>
<br/>
<br/>
<font size="4" >Mizoram, the lesser known North-Eastern state of India is inhabited by various tribes who practise a wide range of religious customs and cultural splendour. Blessed with a diverse range of geographical features, this small state brings excellent opportunities of travelling over the small hillocks with winding trails where one can get the adventurous feel of mountain biking. The landscape of Phawngpui Hills, Vantawang Falls or even Palak Lake looks incredibly picturesque whereas the heritage sites at Chhingpui talk about the forgotten past of the Mizo life. Typical food habit, dance forms, attires, and local music add zing to the overall cultural panorama of Mizoram and tend to be a great deal for any travel enthusiast who would love to taste offbeaten.
</font>
<br/>
<h2>Phawngpui Hills </h2>
<img src="images/Phawngpui.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Located in Chhimtuipui District near Myanmar border, Phawngpui Hills is the highest mountain peak in Mizoram and claimed to be one of the best places to visit in Mizoram. Dubbed as the ‘House of Gods’, this quaint hill station is rich in vibrant species of orchids, fragrant herbs, exotic fauna and colourful rhododendron. The panoramic views of the lush green hillocks spread across the region of Phawngpui and the bamboo groves enriched surroundings are must witness during your trip to Mizoram.
</font>
<br/>
<br/>
<h2>Champhai</h2>
<img src="images/champhaijpg.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Located at an altitude of 1678 m, Champhai overlooks the largest plains in the state of Mizoram. It offers a spectacular view of Myanmar’s hill ranges. It is famously referred to as the ‘Fruit Bowl of Mizoram’. It is also home to ancient relics and monuments, which reflect the rich past of the land. Given below are some of the leading tourist attractions in Champhai.
<ul>
   <li> Murlen National Park</li>
   <li> Rih Dil Lake</li>
   <li> Kawlkulh</li>
    <li>Phawngpui Peak</li>
    <li>Lengteng Hills</li>
   <li> Lengteng Wildlife Sanctuary</li>
    <li>Lamsial Puk</li>
    <li>Palak Wildlife Sanctuary</li>
    <li>Fiara Tui</li>
    <li>Kungawrhi Puk</li>
    <li>Mura Puk</li>
    <li>Thasiama Seno Neihna</li>
    <li>Hnahlan</li>
    <li>Lalruanga Lung</li>

</ul>
</font>
<h2>Aizawl </h2>
<img src="images/Aizawl.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >The largest city in Mizoram and the state’s capital, Aizawl, is located in a picturesque setting on the ridges of hills at an altitude of 1132 m above sea level overlooking valley and hill ranges beyond. Rightly called the ‘Home of Highlanders’, Aizawl also boasts of cultural richness. Here are the leading destinations in Aizawl.
<ul>
<li>    Durtlang Hills</li>
 <li>   Bung</li>
    <li>Tamdil Lake</li>
    <li>Paikhai</li>
    <li>Mini Zoological Garden</li>
