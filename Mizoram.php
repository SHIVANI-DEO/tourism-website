About 95% of current Mizoram population is of diverse tribal origins who settled in the state, mostly from southeast Asia, over waves of migration starting about 16th century but mainly in 18th century. This is the highest concentration of tribal people among all states of India, and they are currently protected under Indian constitution as Scheduled Tribe. The tribes converted from Animist religions to Christianity over the first half of 20th century. </font>
