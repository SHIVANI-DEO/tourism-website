<font size="4" color="grey">
<b>Weather & Rainfall</b>
<br/>
<br/>
Mizoram has a mild climate, relatively cool in summer 20 to 29 °C (68 to 84 °F) and winter temperatures range from 7 to 22 °C (45 to 72 °F). The region is influenced by monsoons, raining heavily from May to September with little rain in the dry (cold) season. The climate pattern is moist tropical to moist sub-tropical, with average state rainfall 254 centimetres (100 in) per annum. <br/>
In the capital Aizawl, rainfall is about 215 centimetres (85 in) and in Lunglei, another major centre, about 350 centimetres (140 in). The state is in a region where cyclones and landslides can cause weather-related emergencies. 
