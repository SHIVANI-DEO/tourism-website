   <li> Luangmual Handicrafts Centre</li>
   <li> Sibuta Lung</li>
    <li>Khawnlung Wildlife Santuary</li>
</ul>
</font>
<br/>
<br/>
<h2>Hmuifang Tlang </h2>
<img src="images/Hmuifang.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >It is a hill which is around 1524m high, 50 km from Aizawl and is a perfect place for mountain biking. For those seeking an adventure you just can’t miss this place. The trails are quite challenging so you better gear up for some thrill. The various trails which can be explored are Trail Baktawng, Trail Sialsuk, and Trail Lungleng. For the not so experienced bike riders you can explore the hill top of Chawilung/Hmuifang/Sumsuih/Thiak village.
</font>
