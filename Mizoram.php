Mizoram is one of three states of India with a Christian majority (87%). Mizoram is a highly literate agrarian economy, but suffers from slash-and-burn jhum or shifting cultivation, and poor crop yields. In recent years, the jhum farming practices are steadily being replaced with a significant horticulture and bamboo products industry. The state's gross state domestic product for 2012 was estimated at 6991 crore (US$1.1 billion). About 20% of Mizoram's population lives below poverty line, with 35% rural poverty.The state has about 871 kilometers of national highways, with NH-54 and NH-150 connecting it to Assam and Manipur respectively. It is also a growing transit point for trade with Myanmar and Bangladesh. <br/>
