<font size="4">
<p>
Located south of the eastern Himalayas, Assam (Asom, Axom) comprises the Brahmaputra Valley and the Barak river valleys along with the Karbi Anglong and the North Cachar Hills with an area of 78,438 km². Assam is surrounded by six of the other: Arunachal Pradesh, Nagaland, Manipur, Mizoram, Tripura, and Meghalaya. Geographically Assam and these states are connected to the rest of India via a strip of land in West Bengal called the Siliguri Corridor or "Chicken's Neck". Assam shares international borders with Bhutan and Bangladesh; and cultures, peoples and climate with South-East Asia. Assam became a part of the British India after the British occupied the region following the First Anglo-Burmese War of 1824–1826.</p>
</font>
