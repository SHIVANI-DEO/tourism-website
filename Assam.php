<a name="Destinations"><font size="6" color="maroon"> <b>Destinations</b></font> </a>
<br/>
<br/>
<font size="4" >Assam gets you wondering! It truly does! Catching the eyes of nature lovers and adventure enthusiasts, Assam is an absolute delight!  AND if boasting is not a sin may I say that even the veteran photographers have put Assam in their Good Books! This North Eastern state flaunts its natural beauty and its ever so delightful culture. The tribal traditions, the sprawling tea gardens, the riverine islands, the Satras, abundant wildlife, majestic Brahmaputra, exotic Bihu dance, fragrant cuisine and gorgeous people are the true identity of this amazing destination called Assam. It is one of those places in India that gives you the notion that the country is absolutely diverse and filled with colours that the world has no clue about! Here is the list of best places to visit in Assam that will help you explore more about this Golden Land:
</font><br/>
<h1>Majuli, the Largest River Island in the World:</h1>
<img src="images/Majuli.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Spreading over 1,250sq.km, Majuli is surrounded by the mighty Brahmaputra and is known for its vibrant cultures and traditions. Home to the natives called ‘Misings’; this fresh water island is in fact the largest river island in the world. However, over the last few decades, global warming and other environmental issues has given rise to soil erosion and has endangered the existence of Majuli.
</font>
<br/>
<br/>
<h2>Haflong, the Switzerland of the East: </h2>
<img src="images/haflong.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Also known as the ‘Ant Hill Town’ or the ‘Switzerland of the East’, Haflong is the only hill station in Assam. Rolling hills and mountains, pleasant climate, panoramic views, picturesque valleys and meadows are the main attraction of this quaint town.
</font>
<br/>
<br/>
<h2>Tea Gardens in Dibrugarh: </h2>
<img src="images/Dibrugarh.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >If you are drinking a cup of the finest quality tea anywhere in the world, there are more than 70% chances that the tea was blended in Dibrugarh! Also known as the ‘Tea City of India’, this town in Assam is known for its finest blends of tea leaves and is also the largest tea producing town in the world.
</font>
<br/>
<br/>
<h2>River Cruise in the Brahmaputra: </h2>
<img src="images/Brahmaputra.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Flowing through the entire length and breadth of the state, the mighty Brahmaputra is also an ideal river cruise destination in the country. With its origin at the great Himalayan Range, a cruise down this stunning river will take you through most of the remarkable and stunning spots in Assam. In some parts of the river, you can also experience river rafting and delightful boat rides.
</font>
<br/>
<br/>
<h2>Silky Visit to Sualkuchi: </h2>
<img src="images/Sualkuchi.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Popularly called the ‘Manchester of Assam’, Sualkuchi is the home to some of the finest silk producing units in the country. The silks used in the colourful Assamese attires are majorly crafted from Sualkuchi silks. Not only silk, this town is also known for its mixed diversity and pictorial beauty.
</font>
