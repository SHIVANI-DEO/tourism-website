<a name="Destinations"><font size="6" color="maroon" > <b>Destinations</b></font> </a>
<br/>
<br/>
<font size="4" >There are times you are left to wonder if nature has been more kind to some parts of the earth. The world has many places that offer you great experiences, breathtaking moments and amazing views. Some tourist spots have beautiful mountains and stunning valleys; some are deserts but still have a great deal to offer. Each is beautiful in its own way and every place is unique for what it has to offer. While nature plays its game at its will on various locations, mankind had been playing its own game. Certain places are rich with man made monuments and arts, while some have nothing special to boast of in terms of human being’s contribution. Very few places have the best of everything and Arunachal Pradesh is one such place. The state is rich with picturesque beauty that offers relaxation and rejuvenation.
</font><br/>
<h1>Tawang </h1>
<img src="IMAGES/tawang.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >if you want to have a look at the historical sites set on an emerald green meadow, Tawang is the ideal place for you. The entire Tawang region is nestled amidst Gudpi and Chong Chugmi ranges and bathed by the water of Tawang Chu River. Tawang Monastery is an around 113 feet long and 80 feet wide edifice which is a famous Buddhist temple. The interior of the building is stunningly beautiful and a 28 feet high golden statue of Lord Buddha is worshipped here.  Indo-China border is another significant spot in Tawang. Jaswant Garh, Tawang War Memorial, Tawang Craft Centre, Brahma-dung-chung Ani Gompa, Urgelling Gompa are some of the important sightseeing places. Madhuri Lake and Pangateng Tso Lake exude enchanting view. Do not forget to have a glimpse of Bap Teng Kang Waterfall and Nuranang Falls. The best time to visit Tawang is May. 2-3 days tour will be enough to explore the land up to the brim.
</font>
<br/>
<br/>
<font color="blue" size="4">How to reach: -</font> <font size="4" >Tezpur is the nearest airport while Rangapara is the nearest rail station. Bus and taxi services are available on demand.						
</font>
<br/>
<br/>
<h2>Roing </h2>
<img src="IMAGES/roing.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Whether you are planning for a tour with your better half or with your buddies, the diverse land of Roing will present you ultimate satisfaction. This picturesque valley is situated on the bank of Dibang River. Geographically this tranquil tourist spot is located in the Dibang Valley District of Arunachal Pradesh. Nehru Van Udyan is a breath-taking forest park set on the riverbank at Deopani. While going from Roing to Bismaknagar, you can see the vibrant orange orchards.
</font>
<br/>
<h2>Bhalukpong</h2>
<font size="4" >
The gateway to Arunachal, Bhalukpong is only 56 km away from Tezpur Town. Located in West Kameng District, this is a historical place where one can look back to the days of the Mahabharata. Nestled amidst the dense forest and rinsed by the Kameng River, the region is a popular picnic spot. For the adventure lovers, there are abundant scopes to explore the region to the fullest. Different levels of trekking can be enjoyed here. You can enjoy rafting on Kameng River or get involved in angling on the bank of this mighty river. Have a glimpse of Pakhui Wildlife Sanctuary which is very close to Bhalukpung. If you are visiting the place in March, you can enjoy the local Nyethidow Festival.
</font><br/>
<br/>
<font color="blue" size="4">How to reach: -</font> <font size="4" > Nearest airport is Tezpur/ Guwahati. Tezpur is the nearest railway station. Bus and taxi are available from both the stops.
</font>
<br/>
<h2>Itanagar </h2>
<img src="IMAGES/Itanagar.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >One of the most popular tourist destinations in Arunachal Pradesh, Itanagar is the state capital and a well connected city. The township is situated at an altitude of 350 m above sea level. Apart from the state administrative headquarters, the city presents a number of rich architectural wonders. The historical Ita Fort is made of special bricks and is significant from a socio-cultural point. For a simple holidaymaker to an archeologist, this place can be of great value to be explored. The legendary Ganga Lake (Gyakar Sinwi) is a famous picnic spot in Itanagar which is 6 km from the heart of the city. Jawaharlal Nehru Museum is a wonderful repository of indigenous tribal culture of Arunachal. Gompa Buddha Vihar is an astounding yellow roofed shrine. One can have a glimpse of the surrounding countryside from the gompa premise. Buy some local artifacts, wall paintings, herbal dyed yarns and bamboo/cane-made attractive utility items from Craft Centre and Emporium. Polo Park, Indira Gandhi Park, and Zoological Park are some of the evening leisure walk spots.

</font>
<br/>
<br/>
<font color="blue" size="4">How to reach: -</font> <font size="4" color="blue">
<br/> <br/>By Bus: – </font><font size=4">Regular luxury bus services are available from Guwahati. It will take approx 11 hours to reach.
</font><br/><br/>
<font size="4" color="blue">
By Air: – </font><font size=4">From Guwahati to Naharlagun (Itanagar) Pawan Hans Helicopter Service is available for making the travel convenient, whereas Tezpur is the nearest airport.
</font>
<br/><br/>
<h2>Parasuram Kund </h2>
<img src="IMAGES/Parasuram-Kund.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >If you are heading to the mystic land of Arunachal Pradesh in January, remember to participate in the Makar Sankranti Mela at the site of Parasuram Kund. A famous Hindu pilgrimage known for its magnetic natural ambience, Parasuram Kund is located 13 miles north-east of Tezu. Well connected by road from Tinsukia and Dibrugarh this holy place should be visited during November to February for witnessing the best views. Take a dip in the holy water of Brahma Kund for attaining penance.
</font>
<br/>
<h2>Tezu
</h2>
<font size="4" >
Another must-visit tourist spot in Arunachal Pradesh is Tezu, the headquarter of Lohit District. Well connected by road from Tinsukia, this is the land of picturesque landscape. Undulating hillocks, gushing blue water, and rocky mountains make it an ideal getaway. Add to that the local Mishmi tribe and you will get to know about a completely different world. There are a numbers of tribes in this district and many of them are yet unaware about the complexities of city life. During festive season their traditional games, dances, and sports are ideal option for merrymaking. Riding (elephants), rafting, angling, mountaineering, hitchhiking, etc. are the entertainment options in Tezu.
</font><br/>
<br/>
<br/>
<br/>
<font color="blue" size="4">How to reach: -</font> <font size="4" >Direct Arunachal Pradesh state transport (APST) bus service connects Tinsukia with Tezu. Nearest Airports are Dibrugarh and Guwahati.
</font>
