<b>By Rail: </b>Dimapur has a railway station on the main line of the North east Frontier Railway. It is well connected to Guwahati, with a number of trains that shuttle between the two stations every day. Guwahati, on the other hand, has direct train lines with most major Indian cities. <br/>
<br/>
<b>By Road: </b>A number of taxis ply from Dimapur to Kohima. Night buses connect Dimapur and Kohima to Guwahati and Shillong (Meghalaya). A very convenient way for reaching Kohima is to take the train or flight to Dimapur and then rent a taxi to Kohima. The Dimapur taxi stand is adjacent to the Railway Station. Yellow taxis can be hired with other passengers (shared taxi) or exclusive (full taxi). A full taxi could cost anything between USD $14 to USD $18 for a one way trip to Kohima. A traveler can also opt to rent a car from Guwahati (through tour operators) and drive all the way to Kohima. This is a very pleasant drive, lasting about 6-7 hours and taking the tourist past beautiful stretches of forests, greenery and hills; but one should try to start and end the journey before nightfall.
