<b>Weather & Rainfall</b>
<br/>
<br/>
Nagaland has a largely monsoon climate with high humidity levels. Annual rainfall averages around 70–100 inches (1,800–2,500 mm), concentrated in the months of May to September. Temperatures range from 70 °F (21 °C) to 104 °F (40 °C). In winter, temperatures do not generally drop below 39 °F (4 °C), but frost is common at high elevations. The state enjoys a salubrious climate. Summer is the shortest season in the state that lasts for only a few months.
<br/>
<br/>
<b>How to reach</b>
<br/>
<br/>
<b>By Air:</b>DIMAPUR has the only Airport in Nagaland at the moment. Direct flights connect the airport to Guwahati and Kolkota and connections can be made from all other airports in India. 
