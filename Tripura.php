<p>
Tripura a state in Northeast India. The third-smallest state in the country and is bordered by Bangladesh(East Bengal) to the north, south, and west, and the Indian states of Assam and Mizoram to the east. The Kokborok speaking Tripuri people are the major group among 19 tribes and many subtribes. The Bengali people form the ethno-linguistic majority in Tripura. </p>
