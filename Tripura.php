<a name="Destinations"><font size="6" color="maroon"> <b>Destinations</b></font> </a>
<br/>
<br/>
<br/>
<font size="4" >One of the Seven Sister States in northeast India, Tripura, is a dream destination for nature lovers. It is surrounded on almost all sides by Bangladesh, the neighboring country. Unexplored forests, breathtaking valleys, gorgeous waterfalls and majestic mountains make Tripura a perfect land to unwind. The land is also rich in culture, which is reflected by the monuments and heritage sites here. Its ancient history and culture compel history lovers to visit the place.
</font>
<br/>
<h2>Agartala </h2>
<img src="images/Agatala.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Agartala would naturally be the first choice destination for those who visit Tripura. The land is rich in natural beauty and also boasts of being home to various tribes. Hence, it is a colorful representation of various cultures. Agartala is famous for beautiful palaces, monuments, temples and handicrafts. It has a rich past too. The monuments here are stunning reminders of the glorious past of Agartala. If you intend to tour Tripura, do not miss the top attractions in Agartala.
<ul><b>
  <li>  Chittangong Hills</li>
<li>    Pilak</li>
   <li> Ujjayanta Palace</li>
   <li> Neermahal Palace</li>
  <li>  Kamalasagar</li>
   <li> Tripura Government Museum</li>
  <li>  State Museum</li>
    <li>Trishna Wildlife Sanctuary</li>
   <li> Gondacherra Wildlife Sanctuary</li>
   <li> Kalapania Nature Park</li>
   <li> Malancha Niwas</li>
   <li> Haveli Museum</li>
</b></ul>
</font>
<br/>
<h2> Jagannath Temple </h2>
<img src=" images/Jagannath.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >The Jagannath Temple is built within the Ujjayanta Palace grounds, specially crafted to decorate the Manikya capital city. Islamic style of architecture is predominant in the exterior of this temple, but the interiors are decorated with Hindu splendour. 
</font>
<br/>
<h2>Udaipur </h2>
<img src="images/Udaipur.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Located 55kms away from Agartala, Udaipur ranks third in the list of largest cities in Tripura. It is on the banks of River Gomati, the largest in Tripura. Famously referred to as the Lake City, it has also a number of temples and historic sites that attract tourists in great numbers. Here are some important places to visit in Udaipur.
<ul>
<b>   <li> Tripura Sundari Temple </li>
<li>    Gunabati Group of Temples</li>
    <li> Old Palace</li>
    <li> Sepahijala Wildlife Sanctuary</li>
    <li> Bhubaneshwari Temple</li>
    <li> Kalyan Sagar</li>
   <li>  Nazrul Granthagar</li>
   <li>  Tepania Eco-Park </li></b>
</ul>
</font>
<br/>
<h2> Ujjayanta Palace </h2>
<img src="images/5622.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >Ujjayanta Palace was a royal palace. Situated at the core of Agartala, the entire hustle-bustle of the city is centered around this palace. Built in 1901, it has magnificent tiled floors, curved wooden ceilings and lovely doors. The name 'Ujjayanta Palace' was given by Rabindranath Tagore, a regular visitor of Tripura. The State has owns a lineage of an independent royal state. The palace includes the Public halls, Throned room, Durbar Hall, Library, The Chinese Room and The Reception Hall.
</font>
<br/>
<h2> Chittangong Hills </h2>
<img src="images/4028.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >The picturesque spectacle of the Chittagong hills assaults the senses of each and every onlooker. The hills comprise of mini mountains with multiple valleys and a small canyon, with 7 rivers meandering across the valleys.
</font>
