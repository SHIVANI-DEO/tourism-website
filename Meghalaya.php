<a name="Destinations"><font size="6" color="maroon"> <b>Destinations</b></font> </a>
<br/>
<br/>
<br/>
<font size="4" >Meghalaya is one of the most visited destination of north east, because of the diversity of natural aesthetic bounty here. It is considered the hotspot of all things cultural and natural existing in sheer harmony with the constructive human efforts. To date, it remains one of those rare locations that can be visited all year round and still over compensate every travellers thirst for a fulfilling retreat.
</font>
<br/>
<h2>Ialong Park (Jowai) </h2>
<img src="images/Park.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >The Ialong Park, about 10 kms from Jowai, is a hidden gem, often left out from plans made to the state of Meghalaya, and something you shouldn’t miss. If you’re planning to visit Meghalaya for a longer period, the Ialong Park is where you need to be to unwind and relax from the overwhelming views and treks during your trip.  The view of the Myndtu river and valley is lush green and a tonic for tired eyes. The state government has taken initiatives to make the Park more environmentally relevant. Leisurely activities like fishing and walks are a great way to relax and take in the natural landscape of Meghalaya.
</font>
<br/>
<br/>
<h2> Umiam Lake (Ri Bhoi District, Nongpoh) </h2>
<img src="images/Umiam.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >If you plan to visit Umiam Lake in the winter time, from October to February, you’ll be dumbfounded by its beauty. If you plan to visit it in the summer months of May to July, you won’t believe it’s the same lake. Located in Nongpoh, Meghalaya The Umiam Lake is basically made from the reservoir built due to the dam here. The lake is the chief source of water supply to the city of Shillong. During the winter months, the lake fills up with water that gives it an unbelievably picturesque beauty. Most travelers to Meghalaya even like to compare it to the beauty of the Scottish water bodies. During the summer months, the water level lowers and islands actually begin to pop up on the surface of the lake. While the water sports here are expensive, but a group water boat rowing into the evening horizon is some money well spent.
</font>
<br/>
<br/>
<h2>Elephant Falls (Shillong) </h2>
<img src="images/Elephantsfall.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >The Elephant Falls are a unique feature of Shillong, since the waterfall itself is made up of three very lucid steps. The first one, hidden among the trees, the second one, tamed and controlled flowing from a certain height, and the third, the fastest and biggest one, culminating into the stream below. Unfortunately, the structure of the black rocks resembling an elephant was destroyed during an earthquake in Meghalaya, but the falls are equally majestic still, with the lucid contrast of the frothy falls against the dark rocks. The evening view of Elephant falls in Meghalaya is the one that wins hearts over, combined with the softly fading light and the high contrast falls, the crowd is worth braving for the sheer gold glow of the water. The sheer natural beauty and serenity of these falls makes it one of the places to visit in Meghalaya.
</font>
<br/>
<br/>
<h2>Siju Cave (Tura) </h2>
<img src="images/Siju.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >The Siju Cave located in Meghalaya, since the beginning of twentieth century, remains one of the chief source of observing natural phenomenon among cave researchers. It  is the first limelighted natural cave in India, is also known as the bat cave, owing to the abundance of these nocturnal mammals here. Also noteworthy, is the limestone formations named after the princes, and the imposing stalagmites and stalactites which constitute the longest existing cave in India, and is a source of major water bodies of the region. Excavators and tourists awe alike at the naturally occurring physical phenomenons of this cave in Meghalaya.
</font>
<br/>
<br/>
<h2> Don Bosco Centre for Indigeneous Culture (Mawlai, Shillong) </h2>
<img src="images/DonBosco.jpg" width="100%"/>	
<br/>
<br/>
<font size="4" >If you spell out travelling as a lesson in history and anthropology, then the DBCIC in Meghalaya is the one place you shouldn’t miss for the world. The Museum, although privately run, is in an excellent condition and proves to be a comprehensive source on the history and life in the north east. The library housed in the museum is a boon for those looking to research, or even brush up their knowledge of the state and its 6 sisters. And as is the aesthetic blessing that every north eastern destination has, the DBCIC does not dissapoint in that department either with the skywalk (not meant for those with vertigo or for the faint hearted), which offers a magnificent view of Shillong.
</font>
